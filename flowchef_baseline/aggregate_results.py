"""Aggregate FlowChef baseline results into deliverables for the paper.

Outputs:
  - winrates.json           : final FlowChef alwayson + dataside numbers
  - latex_table_rows.tex    : exact rows ready to paste into Tab. 3 (tab:fm_main)
  - results_writeup.md      : single-paragraph results writeup for Section 4.3
"""
from __future__ import annotations
import json
import glob
import os
from pathlib import Path

# Legacy aggregator from the development environment. The generic, repo-relative
# replacement is eval/aggregate_table2.py — prefer that for new runs.
ROOT = Path(os.environ.get(
    "PG_MAP_FLOWCHEF_ROOT",
    str(Path(__file__).resolve().parent),
))
EVAL = Path(os.environ.get("PG_MAP_FLOWCHEF_EVAL", str(ROOT / "eval_results")))


def load_summary(scores_summary_path):
    if not scores_summary_path.exists():
        return None
    sm = json.loads(scores_summary_path.read_text())
    out = {}
    for m in ["pickscore", "hps", "clip", "aes"]:
        d = sm.get(m, {})
        out[m] = {
            "win_rate": d.get("win_rate", 0),
            "mean_delta": d.get("mean_delta", 0),
            "n": d.get("n", 0),
        }
    return out


def load_bootstrap(boot_path):
    if not boot_path.exists():
        return {}
    return json.loads(boot_path.read_text())


def load_wilcoxon(wil_path):
    if not wil_path.exists():
        return {}
    return json.loads(wil_path.read_text())


def find_run(pattern):
    matches = sorted(EVAL.glob(pattern))
    return matches[-1] if matches else None


def main():
    out = {}

    # Find headline runs (most recent matching directories)
    aon_dir = find_run("n1632_K1_alwayson_eta*_seed123")
    ds_dir  = find_run("n1632_K1_dataside_eta*_seed123")

    for label, run_dir in [("flowchef_alwayson", aon_dir),
                            ("flowchef_dataside", ds_dir)]:
        if run_dir is None:
            print(f"[warn] no run for {label}")
            continue
        v_dir = run_dir / "flow_flowchef"
        sm = load_summary(v_dir / "scores_summary.json")
        boot = load_bootstrap(v_dir / "bootstrap.json")
        wil  = load_wilcoxon(v_dir / "wilcoxon.json")
        if sm is None:
            print(f"[warn] no scores for {label} at {v_dir}")
            continue
        entry = {
            "run_dir": str(run_dir),
            "PS":  sm["pickscore"]["win_rate"],
            "HPS": sm["hps"]["win_rate"],
            "CLIP": sm["clip"]["win_rate"],
            "Aes": sm["aes"]["win_rate"],
            "PS_mean_delta": sm["pickscore"]["mean_delta"],
            "n": sm["pickscore"]["n"],
            "wilcoxon_p_PS":  wil.get("pickscore", {}).get("p_value"),
            "wilcoxon_p_HPS": wil.get("hps", {}).get("p_value"),
            "ci95_PS": boot.get("pickscore", {}).get("ci_95", None),
        }
        out[label] = entry
        print(f"\n{label}  ({run_dir.name})")
        for m in ["PS", "HPS", "CLIP", "Aes"]:
            print(f"  {m:>4s} = {entry[m]*100:.1f}%")
        print(f"  Wilcoxon p_PS = {entry['wilcoxon_p_PS']}")

    # 5-seed stability
    stab_path = EVAL / "stability_5seed.json"
    if stab_path.exists():
        out["5seed_stability"] = json.loads(stab_path.read_text())

    # eta sweep
    sweep = {}
    for d in sorted(EVAL.glob("eta_sweep_K1_data_eta*_n200")):
        eta = d.name.split("eta")[-1].split("_")[0]
        sm = load_summary(d / "flow_flowchef" / "scores_summary.json")
        if sm:
            sweep[eta] = {m: sm[m]["win_rate"] for m in ["pickscore","hps","clip","aes"]}
    out["eta_sweep_n200"] = sweep

    # Save winrates.json
    (ROOT / "winrates.json").write_text(json.dumps(out, indent=2))
    print(f"\nSaved {ROOT/'winrates.json'}")

    # ---- LaTeX rows ----
    latex_lines = []
    latex_lines.append("% Auto-generated by flowchef_baseline_20260505/aggregate_results.py")
    latex_lines.append("% Insert into Tab. 3 (tab:fm_main) of the main paper.")
    latex_lines.append("")
    for key, label_tex in [("flowchef_alwayson", "FlowChef always-on (K{=}1, $\\eta_z{=}\\eta^\\star$)"),
                            ("flowchef_dataside", "FlowChef data-side (K{=}1, $\\eta_z{=}\\eta^\\star$, M2-gated)")]:
        if key not in out: continue
        e = out[key]
        def cell(wr, p):
            sig = ""
            if p is not None and p < 1e-100: sig = "^{***}"
            elif p is not None and p < 1e-3: sig = "^{***}"
            elif p is not None and p < 0.05: sig = "^{*}"
            return f"${wr*100:.1f}\\%{sig}$"
        latex_lines.append(f"{label_tex} & "
                           f"{cell(e['PS'],  e.get('wilcoxon_p_PS'))} & "
                           f"{cell(e['Aes'], None)} & "
                           f"{cell(e['HPS'], e.get('wilcoxon_p_HPS'))} & "
                           f"{cell(e['CLIP'], None)} \\\\")
    (ROOT / "latex_table_rows.tex").write_text("\n".join(latex_lines))
    print(f"Saved {ROOT/'latex_table_rows.tex'}")

    # ---- One-paragraph writeup ----
    if "flowchef_alwayson" in out and "flowchef_dataside" in out:
        aon = out["flowchef_alwayson"]
        ds  = out["flowchef_dataside"]
        ugfm_ps = 91.9
        ugfm_hps = 75.7
        gap_ps_ds  = ugfm_ps  - ds["PS"]*100
        gap_hps_ds = ugfm_hps - ds["HPS"]*100
        which_gate_better = "data-side" if ds["PS"] > aon["PS"] else "always-on"
        gate_gap = abs(ds["PS"] - aon["PS"]) * 100
        para = (
            "\\paragraph{Head-to-head against FlowChef.} On the same SD3.5-medium "
            "backbone, same scheduler ($28$ steps, cfg $7.0$, $1024^2$), same "
            "PartiPrompts split ($n{=}1632$, seed $123$), and the same shared "
            "baseline images, FlowChef's gradient-skipping schema "
            f"\\citep{{patel2025flowchef}} attains {aon['PS']*100:.1f}\\% PickScore (always-on) and "
            f"{ds['PS']*100:.1f}\\% (data-side, M2-gated) versus PG-MAP's UG-FM at "
            f"{ugfm_ps:.1f}\\% under matched compute --- a gap of "
            f"{gap_ps_ds:+.1f}\\,pp on PS and {gap_hps_ds:+.1f}\\,pp on HPS. The {which_gate_better} "
            f"gate is the stronger FlowChef configuration ($+{gate_gap:.1f}$\\,pp PS over the other), "
            "but neither reaches UG-FM. Combined with the eta sweep "
            f"(Tab.~\\ref{{tab:flowchef_eta_sweep}}), the residual gap isolates the "
            "full-backprop axis: gradient skipping discards the "
            "$I-(1-t)\\partial_z v_\\theta$ Jacobian factor that is exactly "
            "the M1 term Section~4.4 identifies as data-side-amplifying, "
            "consistent with our framing of UG-FM as the natural FM "
            "specialization of the MAP framework rather than an interchangeable "
            "reward-gradient method."
        )
        (ROOT / "results_writeup.md").write_text(para)
        print(f"Saved {ROOT/'results_writeup.md'}")


if __name__ == "__main__":
    main()
